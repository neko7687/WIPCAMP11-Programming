public class _2_Relational {
    public static void main(String[] args) {
        int x =  98;
        
        System.out.println("== : " + (x == 98));
        
        System.out.println("!= : " + (x != 77));
        
        System.out.println("> : " + (x > 40));
        
        System.out.println("< : " + (x < 500));
        
        System.out.println(">= : " + (x >= 98));
        
        System.out.println("<= : " + (x <= 98));
    }
}
