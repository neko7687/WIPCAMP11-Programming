public class _3_Logical {
    public static void main(String[] args) {
        int x = 9;
        
        System.out.println("|| : " + (x < 4 || x > 4));
        
        System.out.println("&& : " + (x > 1 && x < 10));
        
        System.out.println("Normal : " + (x >= 9));
        
        System.out.println("! : " + !(x >= 5));
    }
}
